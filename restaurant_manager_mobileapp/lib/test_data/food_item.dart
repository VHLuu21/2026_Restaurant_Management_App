class FoodItem {
  final int id;
  final String image;
  final String name;
  final String desc;
  final double rating;
  final int price;
  final String category;

  FoodItem({
    required this.id,
    required this.image,
    required this.name,
    required this.desc,
    required this.rating,
    required this.price,
    required this.category
  });
}